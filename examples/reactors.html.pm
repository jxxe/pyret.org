#lang pollen

◊p{
   ◊link/new-tab["https://pyret.org/docs/horizon/reactors.html#(part._reactors)"]{Reactors} are a mechanism for
   describing interactive computations. Reactors, especially in conjunction with
      ◊link/new-tab["https://pyret.org/docs/horizon/image.html"]{images},
   can be used to build physical simulations.
   These features are used in several curricula such as
   ◊link/new-tab["https://www.bootstrapworld.org/materials/physics/"]{Bootstrap:Physics},
   ◊link/new-tab["https://www.bootstrapworld.org/materials/reactive/"]{Bootstrap:Reactive}, and
   ◊link/new-tab["https://dcic-world.org/"]{DCIC}.
   They are supported by both
   ◊link/new-tab["https://cs.brown.edu/~sk/Publications/Papers/Published/plpk-reactor-design/"]{language development}
   and
   ◊link/new-tab["https://cs.brown.edu/~sk/Publications/Papers/Published/vmflpk-bootstrap-physics/"]{curricular research}.
}

◊p{
   To understand the design, flexibility, and careful interaction of reactors with other language features like testing and tables,
   see our 
   ◊link/new-tab["https://cs.brown.edu/~sk/Publications/Papers/Published/plpk-reactor-design/"]{detailed paper}.
}
